#1.Compliment Operator
print(~12)
'''12=00001100
Compliment of 12=~12=11110011=-13
1's compliment of 13=11110010
2's compliment of (-13)=11110010+1=11110011=12'''
#2.And operator(&)
print(12&13)
'''12=00001100,13=00001101
00001100&00001101=00001100=12'''
#3.Or Operator
print(12|13)
'''12=00001100,13=00001101
00001100|00001101=00001101=13'''
#4.Xor Operator
print(12^13)
'''12=00001100,13=00001101
00001100^00001101=00000001=1'''
#5.Left shift
print(10<<2)
'''10=1010
10<<2=101000=40'''
#6.Right shift
print(10>>2)
'''10=1010
10>>2=10=2'''